import numpy as np
#declaration of array(contains elements of same data type only)
A= np.array([1,2,3,4,5])
B= np.array([1,2,3,4,5,12.7])
C= np.array([1,2,3,4,5,'shiva'])
#printing array
print(A)
print(B)
print(C)
#Type of array
print(type(A))
print(type(B))
print(type(C))
#size of array(rows x coloumns)
print(A.shape)
print(B.shape)
print(C.shape)
#data type of each element in array
print(A.dtype)
print(B.dtype)
print(C.dtype)


